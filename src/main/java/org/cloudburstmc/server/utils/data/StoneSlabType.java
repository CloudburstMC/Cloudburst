package org.cloudburstmc.server.utils.data;

import lombok.RequiredArgsConstructor;
import org.cloudburstmc.server.utils.Identifier;

import static org.cloudburstmc.server.block.BlockIds.*;

@RequiredArgsConstructor
public enum StoneSlabType {
    SMOOTH_STONE(STONE_SLAB, NORMAL_STONE_STAIRS),
    SANDSTONE(STONE_SLAB, SANDSTONE_STAIRS),
    @Deprecated
    WOOD(STONE_SLAB, OAK_STAIRS),
    COBBLESTONE(STONE_SLAB, STONE_STAIRS),
    BRICK(STONE_SLAB, BRICK_STAIRS),
    STONE_BRICK(STONE_SLAB, STONE_BRICK_STAIRS),
    QUARTZ(STONE_SLAB, QUARTZ_STAIRS),
    NETHER_BRICK(STONE_SLAB, NETHER_BRICK_STAIRS),
    RED_SANDSTONE(STONE_SLAB2, RED_SANDSTONE_STAIRS),
    PURPUR(STONE_SLAB2, PURPUR_STAIRS),
    PRISMARINE_ROUGH(STONE_SLAB2, PRISMARINE_STAIRS),
    PRISMARINE_DARK(STONE_SLAB2, DARK_PRISMARINE_STAIRS),
    PRISMARINE_BRICK(STONE_SLAB2, PRISMARINE_BRICKS_STAIRS),
    MOSSY_COBBLESTONE(STONE_SLAB2, MOSSY_COBBLESTONE_STAIRS),
    SMOOTH_SANDSTONE(STONE_SLAB2, SMOOTH_SANDSTONE_STAIRS),
    RED_NETHER_BRICK(STONE_SLAB2, RED_NETHER_BRICK_STAIRS),
    END_STONE_BRICK(STONE_SLAB3, END_BRICK_STAIRS),
    SMOOTH_RED_SANDSTONE(STONE_SLAB3, SMOOTH_RED_SANDSTONE_STAIRS),
    POLISHED_ANDESITE(STONE_SLAB3, POLISHED_ANDESITE_STAIRS),
    ANDESITE(STONE_SLAB3, ANDESITE_STAIRS),
    DIORITE(STONE_SLAB3, DIORITE_STAIRS),
    POLISHED_DIORITE(STONE_SLAB3, POLISHED_DIORITE_STAIRS),
    GRANITE(STONE_SLAB3, GRANITE_STAIRS),
    POLISHED_GRANITE(STONE_SLAB3, POLISHED_GRANITE_STAIRS),
    MOSSY_STONE_BRICK(STONE_SLAB4, MOSSY_STONE_BRICK_STAIRS),
    SMOOTH_QUARTZ(STONE_SLAB4, SMOOTH_QUARTZ_STAIRS),
    STONE(STONE_SLAB4, NORMAL_STONE_STAIRS),
    CUT_SANDSTONE(STONE_SLAB4, SANDSTONE_STAIRS),
    CUT_RED_SANDSTONE(STONE_SLAB4, RED_SANDSTONE_STAIRS),
    BLACKSTONE(BLACKSTONE_SLAB, BLACKSTONE_STAIRS),
    POLISHED_BLACKSTONE(BLACKSTONE_SLAB, POLISHED_BLACKSTONE_STAIRS),
    POLISHED_BLACKSTONE_BRICK(BLACKSTONE_SLAB, POLISHED_BLACKSTONE_BRICK_STAIRS),
    ;

    private final Identifier slabId;
    private final Identifier stairsId;
    private final Identifier wallId;

    StoneSlabType(Identifier slabId, Identifier stairsId) {
        this.slabId = slabId;
        this.stairsId = stairsId;
        this.wallId = null;
    }

    public Identifier getSlabId() {
        return slabId;
    }

    public Identifier getStairsId() {
        return stairsId;
    }

    public Identifier getWallId() {
        return wallId;
    }
}
